<?php

// Define the day of the week
$day = "Domingo";

// Check the day of the week and display a corresponding message
switch ($day) {
    case "Segunda-feira":
        echo "Hoje é segunda-feira!";
        break;
    case "Terça-feira":
        echo "Hoje é terça-feira!";
        break;
    case "Quarta-feira":
        echo "Hoje é quarta-feira!";
        break;
    case "Quinta-feira":
        echo "Hoje é quinta-feira!";
        break;
    case "Sexta-feira":
        echo "Hoje é sexta-feira!";
        break;
    case "Sábado":
        echo "Hoje é sábado!";
        break;
    case "Domingo":
        echo "Hoje é domingo!";
        break;
    default:
        echo "Dia inválido.";
}

?>
