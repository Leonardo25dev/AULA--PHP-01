<?php
$a = 1; // Initialize $a to 1

// Increment $a by 2
$a += 2;
echo $a; // Output: 3

// Subtract 2 from $a
$a -= 2;
echo $a; // Output: 1

// Multiply $a by 2
$a *= 2;
echo $a; // Output: 2

// Divide $a by 2
$a /= 2;
echo $a; // Output: 1

// Increment $a by 1 and return the value
echo ++$a; // Output: 2

// Return the value of $a and then increment by 1
echo $a++; // Output: 2

// Decrement $a by 1 and return the value
echo --$a; // Output: 1

// Return the value of $a and then decrement by 1
echo $a--; // Output: 1
?>

